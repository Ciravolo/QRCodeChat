package com.unipi.iet.qrcodechat;

/**
 * Class that handles the storing of the keys only for the creation of a new user in the list.
 */
public class Constants {

    private String myKey = "";
    private String hisKey = "";


    public String getMyKey() {
        return myKey;
    }

    public void setMyKey(String myKey) {
        this.myKey = myKey;
    }

    public String getHisKey() {
        return hisKey;
    }

    public void setHisKey(String hisKey) {
        this.hisKey = hisKey;
    }

}
